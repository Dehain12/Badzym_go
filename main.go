package main // - объявляем пакет

import "fmt" // - импорт пакета с различными командами, аналог ящик с инструментами.

func main() { // - объявляем кусок кода, где программа начнет свое выполнение
	fmt.Print("Hello World") // Print - просто вывод строки на экран, а Println вывод строки на экран + перенос на новую строку
	fmt.Println("Hello World")
	fmt.Print("Hello World")
}
